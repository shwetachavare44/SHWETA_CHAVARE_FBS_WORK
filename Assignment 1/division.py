#3. Program to find quotient and remainder of two numbers.

dividend = int(input('Enter number to be divided (Dividend) :'))
divisor = int(input('Enter number to divided by (Divisor) :'))

Quotient = dividend // divisor
Remainder = dividend % divisor

print("Results :" )
print(f'Dividend is : {dividend}')
print(f'Divisor is : {divisor}')
print(f'Quotient is : {Quotient}')
print(f'Remainder is : {Remainder}')






